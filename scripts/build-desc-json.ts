function tr(desc: string, locale: L): string {
  const idx = locale === 'fr' ? 1 : 2;
  let result = desc;

  // Sort all phrases by length descending for greedy matching
  const sorted = [...P].sort((a, b) => b[0].length - a[0].length);

  for (const [eng, fr, es] of sorted) {
    const trans = idx === 1 ? fr : es;
    if (result.includes(eng)) {
      result = result.split(eng).join(trans);
    }
  }

  return result;
}

async function main() {
  const { data } = await sb.from('recipes').select('description').order('description');
  const descs = [...new Set(data!.map(d => d.description))].sort();

  const map: Record<string, { fr: string; es: string }> = {};
  let englishRemaining = 0;

  for (const desc of descs) {
    const fr = tr(desc, 'fr');
    const es = tr(desc, 'es');
    map[desc] = { fr, es };

    // Check
    const commonEng = /\b(the|that|this|with|and|for|but|is|are|has|have|was|were|it|its|or|not|an|at|by|no|be|do|if|so|as|up|to|in|of|on|we|he|she|my|am|us)\b/g;
    const frMatches = fr.match(commonEng);
    if (frMatches && frMatches.length > 0) {
      englishRemaining++;
      if (englishRemaining <= 5) {
        console.log(`\n[REMAINING EN in FR] ${frMatches.join(', ')}`);
        console.log(`  EN: ${desc.substring(0, 120)}`);
        console.log(`  FR: ${fr.substring(0, 120)}`);
      }
    }
  }

  console.log(`\n${englishRemaining}/${descs.length} descriptions still have common English words in FR`);

  const outPath = join(process.cwd(), 'scripts', 'description-translations.json');
  writeFileSync(outPath, JSON.stringify(map, null, 2));
  console.log(`Wrote ${Object.keys(map).length} translations to ${outPath}`);
}

main();
